class Magia:

    def __init__(self, nome = str, tipo = str, descricao = str, custo = int, efeito = None):
        self.nome = nome
        self.tipo = tipo
        self.descricao = descricao
        self.custo = custo
        self.efeito = efeito

def definir_nome(self, nome):
    self.nome = nome

def definir_tipo(self, tipo):
    self.tipo = tipo

def definir_descricao(self, descricao):
    self.descricao = descricao

def definir_custo(self, custo):
    self.custo = custo

def definir_efeito(self, efeito):
    self.efeito = efeito

